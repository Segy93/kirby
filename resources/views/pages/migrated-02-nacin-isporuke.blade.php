<h1 class="rtecenter">Način isporuke</h1>
<p class="rtejustify">
    Isporuku robe vršimo na teritorije cele Srbije svakim radnim danom i subotom. Nedeljom isporuke nisu moguće. Dostava se vrši putem kurirske službe D Express i prema opštim pravilima ove kurirske službe, kao i našim dostavnim vozilima na užoj teritoriji grada Beograda. <strong>Okvirni rok isporuke u zavisnosti od mesta isporuke iznosi od 60 minuta do 2 radna dana (danas za sutra). </strong>Za pojedine artikle specifične u svojoj nameni ili artikle koji se nalaze u statusu deficitarne robe na svetskom ili lokalnom tržištu (za šta su odgovorni proizvođači) rok isporuke može biti i do 30 dana od dana poručivanja. U ovakvim i sličnim situacijama naš prodajni tim će Vas obavestiti o vremenu i uslovima isporuke direktno telefonom ili na email radi uspešne realizacije posla.
</p>

<p class="rtejustify">
    Robu poručenu perko našeg Sajta možete podići i lično u našem maloprodajnom objektu koji se nalazi u Kumanovskoj 14, Vračar, 11000, Beograd.
</p>

