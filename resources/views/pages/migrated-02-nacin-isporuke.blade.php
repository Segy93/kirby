<h2 class="rtecenter">Ostali uslovi isporuke</h2>
<p class="rtejustify">
    Izbor načina plaćanja direktno utiče na brzinu isporuke. Ako ste izabrali nekih od bezgotovinskih načina plaćanja, pošiljka se šalje onog trenutka kada evidentiramo uplatu na našem tekućem računu.
</p>

<p class="rtejustify">
    <strong>Kupac je dužan</strong> da prilikom prijema paketa isti prekontroliše u prisustvu kurira. Ukoliko primetite da je paket oštećen, molimo Vas da <u>u prisustvu kurira sačinite zapisnik o oštećenju robe i isti vratite kuriru koji će paket vratiti nama</u>. U ovakvim situacijama mi ćemo Vam po prijemu oštećenog odmah poslati novi uređaj. Svi paketi poslati preko kurirske službe su osigurani i nemate nikakvih razloga za brigu u slučaju evetualnih oštećenja.
</p>

